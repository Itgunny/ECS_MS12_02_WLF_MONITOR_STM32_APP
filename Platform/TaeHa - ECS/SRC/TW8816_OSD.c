void SetDisplayWarning(void)
{
  u8 i;
  WriteTW8816(0x94, 0x02);
  for(i = 0; i < 27; i++){
    WriteTW8816(0x94, 0x80);
    WriteTW8816(0x95, 0x81);
    WriteTW8816(0x96, (i + 0x9b));
    WriteTW8816(0x97, (i + 0x7B));
    WriteTW8816(0x98, 0x06);    
  }
}

